package com.mvc.beans;

public class CustomerReg {
	
	
	private String Cust_Name ;
	private String Cust_E_mail ;
	private long Cust_phno ;
	
	
	public String getCust_Name() {
		return Cust_Name;
	}
	public void setCust_Name(String cust_Name) {
		Cust_Name = cust_Name;
	}
	public String getCust_E_mail() {
		return Cust_E_mail;
	}
	public void setCust_E_mail(String cust_E_mail) {
		Cust_E_mail = cust_E_mail;
	}
	public long getCust_phno() {
		return Cust_phno;
	}
	public void setCust_phno(long cust_phno) {
		Cust_phno = cust_phno;
	}
	
	

}
